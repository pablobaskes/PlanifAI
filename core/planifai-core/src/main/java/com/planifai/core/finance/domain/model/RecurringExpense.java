package com.planifai.core.finance.domain.model;

import java.math.BigDecimal;
import java.time.LocalDate;

public class RecurringExpense {

    private Long id;
    private String name;
    private BigDecimal amount;
    private ExpenseCategory category;
    private RecurringExpenseRecurrence recurrence;
    private Integer paymentDay;
    private LocalDate startDate;
    private LocalDate endDate;
    private Boolean active;
    private String notes;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    public ExpenseCategory getCategory() {
        return category;
    }

    public void setCategory(ExpenseCategory category) {
        this.category = category;
    }

    public RecurringExpenseRecurrence getRecurrence() {
        return recurrence;
    }

    public void setRecurrence(RecurringExpenseRecurrence recurrence) {
        this.recurrence = recurrence;
    }

    public Integer getPaymentDay() {
        return paymentDay;
    }

    public void setPaymentDay(Integer paymentDay) {
        this.paymentDay = paymentDay;
    }

    public LocalDate getStartDate() {
        return startDate;
    }

    public void setStartDate(LocalDate startDate) {
        this.startDate = startDate;
    }

    public LocalDate getEndDate() {
        return endDate;
    }

    public void setEndDate(LocalDate endDate) {
        this.endDate = endDate;
    }

    public Boolean getActive() {
        return active;
    }

    public void setActive(Boolean active) {
        this.active = active;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }
}
